.test
  ld sp, 0xFF80
  ld de, 0xFF81
  ld a, [de]
  add a, 0x1
  ld [de], a
  jr nc, .continue
  dec de
  ld a, [de]
  add a, 0x10
  ld [de], a
.continue
  pop af
  rla
  ld [hld], a
  dec bc
  ld a, b
  or a, c
  jr nz, .test
  ld sp, 0xFF84
  ld bc, 0x1000
  ld de, results
  jp Compare
pass:
  ld de, string
  call printString
.done
  jr .done

sendConsole:
; A = data to be sent
ldh [0xFF86], a
ld a, 0x81
ldh [0xFF02], a
ldh a, [0xFF86]
ldh [0xFF01], a
ret
printString:
ld a, [de]
call sendConsole
inc de
  or a, a
  jr nz, printString
ret
db 0x00
string:
db "You Passed the test!", 10, 0
string2:
db "You Failed the test!", 10, 0
test:
db 0x1b
Compare:
ld a, [de]
inc de
dec bc
inc hl
cp a, [hl]
jr nz, consoleError
ld a, b
or a, c
jr nz, Compare
jp pass

consoleError:
ld de, string2
call printString
.end
jr .end
; Subroutine to copy bc bytes from de to hl
Copy:
  ld a, [de]
  ld [hli], a
  inc de
  dec bc
  ld a, b
  or a, c
  jr nz, Copy
  ret

